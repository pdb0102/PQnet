		public static void sha512_inc_init(byte[] state) {
			for (int i = 0; i < 64; ++i) {
				state[i] = iv_512[i];
			}
			for (int i = 64; i < 72; ++i) {
				state[i] = 0;
			}
		}

		public static void sha512_inc_blocks(byte[] state, byte[] in_buf, int inblocks) {
			ulong bytes = load_bigendian_64(state, 64);

			crypto_hashblocks_sha512(state, in_buf, 128 * (ulong)inblocks);
			bytes += 128 * (ulong)inblocks;

			store_bigendian_64(state, 64, bytes);
		}

		public static void sha512_inc_finalize(byte[] out_buf, byte[] state, byte[] in_buf, int inlen) {
			byte[] padded;
			ulong bytes;
			int in_buf_offset;

			bytes = load_bigendian_64(state, 64) + (ulong)inlen;
			padded = new byte[256];
			in_buf_offset = 0;

			crypto_hashblocks_sha512(state, in_buf, (ulong)inlen);
			in_buf_offset += inlen;
			inlen &= 127;
			in_buf_offset -= inlen;

			for (int i = 0; i < inlen; ++i) {
				padded[i] = in_buf[in_buf_offset + i];
			}
			padded[inlen] = 0x80;

			if (inlen < 112) {
				for (int i = inlen + 1; i < 119; ++i) {
					padded[i] = 0;
				}
				padded[119] = (byte)(bytes >> 61);
				padded[120] = (byte)(bytes >> 53);
				padded[121] = (byte)(bytes >> 45);
				padded[122] = (byte)(bytes >> 37);
				padded[123] = (byte)(bytes >> 29);
				padded[124] = (byte)(bytes >> 21);
				padded[125] = (byte)(bytes >> 13);
				padded[126] = (byte)(bytes >> 5);
				padded[127] = (byte)(bytes << 3);
				crypto_hashblocks_sha512(state, padded, 128);
			} else {
				for (int i = inlen + 1; i < 247; ++i) {
					padded[i] = 0;
				}
				padded[247] = (byte)(bytes >> 61);
				padded[248] = (byte)(bytes >> 53);
				padded[249] = (byte)(bytes >> 45);
				padded[250] = (byte)(bytes >> 37);
				padded[251] = (byte)(bytes >> 29);
				padded[252] = (byte)(bytes >> 21);
				padded[253] = (byte)(bytes >> 13);
				padded[254] = (byte)(bytes >> 5);
				padded[255] = (byte)(bytes << 3);
				crypto_hashblocks_sha512(state, padded, 256);
			}

			for (int i = 0; i < 64; ++i) {
				out_buf[i] = state[i];
			}
		}

		public static void sha512(byte[] out_buf, byte[] in_buf, int inlen) {
			byte[] state;

			state = new byte[72];

			sha512_inc_init(state);
			sha512_inc_finalize(out_buf, state, in_buf, inlen);
		}

		/*
	 * mgf1 function based on the SHA-512 hash function
	 */
		public static void mgf1_512(byte[] out_buf, int outlen, byte[] in_buf, int inlen) {
			byte[] in_tmp;
			byte[] out_tmp;
			int i;
			int out_buf_offset;

			out_tmp = new byte[64];
			in_tmp = new byte[inlen + 4];
			out_buf_offset = 0;

			Array.Copy(in_buf, 0, in_tmp, 0, inlen);

			/* While we can fit in at least another full block of SHA512 output.. */
			for (i = 0; (i + 1) * 64 <= outlen; i++) {
				store_bigendian_32(in_tmp, inlen, (uint)i);
				sha512(out_buf, in_tmp, inlen + 4);
				out_buf_offset += 64;
			}

			/* Until we cannot anymore, and we fill the remainder. */
			if (outlen > i * 64) {
				store_bigendian_32(in_tmp, inlen, (uint)i);
				sha512(out_tmp, in_tmp, inlen + 4);
				Array.Copy(out_tmp, 0, out_buf, out_buf_offset, outlen - (i * 64));
			}
		}

