			return ((ulong)x[offset + 7] & 0xFF) | ((ulong)(x[offset + 6] & 0xFF) << 8) | ((ulong)(x[offset + 5] & 0xFF) << 16) | ((ulong)(x[offset + 4] & 0xFF) << 24) | ((ulong)(x[offset + 3] & 0xFF) << 32) | ((ulong)(x[offset + 2] & 0xFF) << 40) | ((ulong)(x[offset + 1] & 0xFF) << 48) | ((ulong)(x[offset] & 0xFF) << 56);
		}

		[MethodImpl(MethodImplOptions.AggressiveInlining)]
		private static void store_bigendian_32(byte[] x, int offset, uint u) {
			x[offset + 3] = (byte)u;
			x[offset + 2] = (byte)(u >> 8);
			x[offset + 1] = (byte)(u >> 16);
			x[offset] = (byte)(u >> 24);
