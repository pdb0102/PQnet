			crypto_hashblocks_sha512(state, padded, 256);
		}

		for (int i = 0; i < 64; ++i) {
			out_buf[i] = state[i];
		}
	}

	public static void sha512(byte[] out_buf, byte[] in_buf, int inlen) {
		byte[] state;

		state = new byte[72];

		sha512_inc_init(state);
		sha512_inc_finalize(out_buf, state, in_buf, inlen);
	}

	/*
 * mgf1 function based on the SHA-512 hash function
 */
	public static void mgf1_512(byte[] out_buf, int outlen, byte[] in_buf, int inlen) {
		byte[] in_tmp;
		byte[] out_tmp;
		int i;
		int out_buf_offset;

		out_tmp = new byte[64];
		in_tmp = new byte[inlen + 4];
		out_buf_offset = 0;

		Array.Copy(in_buf, 0, in_tmp, 0, inlen);

		/* While we can fit in at least another full block of SHA512 output.. */
		for (i = 0; (i + 1) * 64 <= outlen; i++) {
			store_bigendian_32(in_tmp, inlen, (uint)i);
			sha512(out_buf, in_tmp, inlen + 4);
			out_buf_offset += 64;
		}

		/* Until we cannot anymore, and we fill the remainder. */
		if (outlen > i * 64) {
			store_bigendian_32(in_tmp, inlen, (uint)i);
			sha512(out_tmp, in_tmp, inlen + 4);
			Array.Copy(out_tmp, 0, out_buf, out_buf_offset, outlen - (i * 64));
		}
	}

