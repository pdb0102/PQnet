	public static void sha512_inc_init(byte[] state) {
		for (int i = 0; i < 64; ++i) {
			state[i] = iv_512[i];
		}
		for (int i = 64; i < 72; ++i) {
			state[i] = 0;
		}
	}

	public static void sha512_inc_blocks(byte[] state, byte[] in_buf, int inblocks) {
		ulong bytes = load_bigendian_64(state, 64);

		crypto_hashblocks_sha512(state, in_buf, 128 * (ulong)inblocks);
		bytes += 128 * (ulong)inblocks;

		store_bigendian_64(state, 64, bytes);
	}

	public static void sha512_inc_finalize(byte[] out_buf, byte[] state, byte[] in_buf, int inlen) {
		byte[] padded;
		ulong bytes;
		int in_buf_offset;

		bytes = load_bigendian_64(state, 64) + (ulong)inlen;
		padded = new byte[256];
		in_buf_offset = 0;

		crypto_hashblocks_sha512(state, in_buf, (ulong)inlen);
		in_buf_offset += inlen;
		inlen &= 127;
		in_buf_offset -= inlen;

		for (int i = 0; i < inlen; ++i) {
			padded[i] = in_buf[in_buf_offset + i];
		}
		padded[inlen] = 0x80;

